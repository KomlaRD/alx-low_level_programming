/**
 * _islower- checks for lowercase characters
 *
(* Description: Function checks a given input if it
* is a lowercase character)
* @c: input character
*
* Return: Always 0
*/

#include <main.h>

int _islower(int c)
{
	if (_islower(c) == true)
	{
		_putchar("1\n");
	}
	else
	{
		_putchar("0\n");
	}

	return (0);
}
